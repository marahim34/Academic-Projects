public class App {
	public static void main(String[] args) {
		// Printing special characters
		/*
		 * Newline (\n): Inserts a new line.
		 * Tab (\t): Inserts a tab space.
		 * Backslash (\\): Prints a backslash.
		 * Double quote (\"): Prints a double quote.
		 * Single quote (\'): Prints a single quote.
		 * Carriage return (\r): Moves the cursor to the beginning of the current line
		 * without advancing downward.
		 * Form feed (\f): Advances the cursor to the beginning of the next page (rarely
		 * used).
		 */

		System.out.println("This is a double quote: \"");
		System.out.println("This is a backslash: \\");
		System.out.println("This is a new line:\nNew Line");
		System.out.println("This is a tab:\tTabbed Text");

	}
}